export default function ResellNFT() {
  const [formInput, updateFormInput] = useState({ price: '', image: '' })
  const router = useRouter()
  const { id, tokenURI } = router.query
  const { image, price } = formInput

  const [nfts, setNfts] = useState([])

  useEffect(() => {

    fetchNFT()
  }, [id])

  async function fetchNFT(nft) {
    if (!tokenURI) return
    const meta = await axios.get(tokenURI)
    updateFormInput(state => ({ ...state, image: meta.data.image }))
  }




  async function listNFTForSale() {
    if (!price) return
    const web3Modal = new Web3Modal()
    const connection = await web3Modal.connect()
    const provider = new ethers.providers.Web3Provider(connection)
    const signer = provider.getSigner()

    const priceFormatted = ethers.utils.parseUnits(formInput.price, 'ether')
    let contract = new ethers.Contract(marketplaceAddress, abi, signer)
    let listingPrice = await contract.getListingPrice()

    listingPrice = listingPrice.toString()
    let transaction = await contract.resellToken(id, priceFormatted, { value: listingPrice })
    await transaction.wait()

    router.push('/')
  }

  return (
    <>
  

      <div className='container'>

      </div>

      <div className={`${style.nfts}`}>
        <div className='container rounded '>
          <div className={`${style.nft_card}`}></div>
          <div className='container rounded py-4'></div>
          <div className='row'>
 <div className='col-md-3'>
            {
              image && (
                <img  src={image} className={`${style.nftImage} img-fluid rounded mb-2`} />
                )
              }
              <input
                placeholder="NFT Price (INR)"
                className={` rounded mb-2`}
                onChange={e => updateFormInput({ ...formInput, price: e.target.value })}
              />

            <div className="mb-3 col-md-8 col-sm-12">
              <button onClick={listNFTForSale} className="btn btn-primary text-nowrap lh-lg col-12 fw-bold rounded-pill">
                Resell NFT
              </button>
            </div>
          </div>
        </div>
        </div>



      </div>

    </>
  )

}

